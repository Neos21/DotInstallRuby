=begin
Lesson 1 : Hello World

Ruby のことはじめ。
・単一行コメントは「#」と書く。
・複数行コメントは「=begin」と「=end」行で囲む。
=end

# print : 終わった後に改行がない
print "Hello World!"

# puts : 改行あり
puts
puts "Hello World (puts)"

# p : データ形式が分かるように表示される
p "Hello World (p)"

# 「;」を書けば続けて処理が書ける。
print "Hello "; print "World!"